package org.gravity.adapter;

public class RectF
{
	public float x, y, width, height;

	public RectF(float x, float y, float width, float height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	public RectF()
	{
		this.x = 0;
		this.y = 0;
		this.width = 0;
		this.height = 0;
	}
}
